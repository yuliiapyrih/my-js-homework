// - є сторінка rules.html. Контентом сторінки є заголовки та параграфи. Заголовки (h2) характеризують тему контенту яка вказана в параграфі.
// створити скрипт, котрий зчитує всі заголовки, та робить в блоці з id=content з них список(ul>li), який буде змістом того, що знаходиться на сторінці.
// Скріпт повинен працювати навіть якщо кількість блоків з заголовком та параграфом зміниться.
let allTitle=document.querySelectorAll('h2');
let allTitleText=[];
for (let i = 0; i < allTitle.length; i++) {
    allTitleText[i]=allTitle[i].innerText;
}
let contentID=document.getElementById('content');
let elemUL =document.createElement('ul');
for (let j = 0; j < allTitle.length; j++) {
    let elemLI=document.createElement('li');
    elemLI.innerHTML=allTitleText[j];
    elemUL.appendChild(elemLI);
}
contentID.appendChild(elemUL);

